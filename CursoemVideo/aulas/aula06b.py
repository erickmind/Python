n = input('Digite algo: ')
print(n.isnumeric())  # É um número
print(n.isalpha())  # É alfabético
print(n.isalnum())  # É alfanumérico
print(n.isupper())  # Está em letras maiúsculas

booleano = bool(input('Digite um valor: '))
